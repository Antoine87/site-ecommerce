<?php
class FormatDTO {

    private $idFormat;
private $format;

    public function setIdFormat($idFormat){
            $this->idFormat = $idFormat;
        }
public function getIdFormat(){
            return $this->idFormat;
        }
public function setFormat($format){
            $this->format = $format;
        }
public function getFormat(){
            return $this->format;
        }

}